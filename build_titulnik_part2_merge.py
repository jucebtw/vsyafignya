def expand_block(title: str, base: str, extra_paragraphs: list[str]) -> list[str]:
    out = [title, ""]
    out.extend(wrap_paragraphs(base))
    out.append("")
    out.extend(extra_paragraphs)
    out.append("")
    return out


def wrap_paragraphs(s: str) -> list[str]:
    """Разбить длинный текст на абзацы по пустым строкам и по длине."""
    parts = [p.strip() for p in re.split(r"\n\s*\n", s) if p.strip()]
    return parts


