# Точка разрыва: начало раздела «2. Индивидуальное задание» в теле отчёта (не оглавление)
SPLIT_MARKER_BODY = "\x0c2. Индивидуальное задание"


def decode_doc_utf16(path: Path) -> str:
    data = path.read_bytes()
    return data.decode("utf-16-le", errors="ignore")


def extract_part1_lines(full_text: str) -> list[str]:
    """Строки части 1: от первого осмысленного блока до конца 1.6.6."""
    idx = full_text.find(SPLIT_MARKER_BODY)
    if idx == -1:
        raise RuntimeError("Не найден маркер раздела 2 в теле документа")
    part1 = full_text[:idx].replace("\x0c", "\n")
    lines = []
    for raw in re.split(r"[\r\n]+", part1):
        line = raw.strip()
        if not line:
            continue
        # отбросить мусор в начале
        if "КГБПОУ" in line or "ОТЧЕТ" in line or "Введение" in line:
            pass
        elif len(line) < 4:
            continue
        if line.startswith("쿐") or line.startswith("bjbj"):
            continue
        lines.append(raw.rstrip())
    # Нормализуем: собрать непрерывный текст как в оригинале — построчно для параграфов
    return [x for x in re.split(r"[\r\n]+", part1) if x.strip()]


def strip_toc_field_noise(line: str) -> str:
    """Упростить строки оглавления: оставить читаемый текст (поля Word не переносятся в OOXML как в .doc)."""
    if "HYPERLINK" not in line and "PAGEREF" not in line and "TOC" not in line:
        return line
    # вытащить фрагменты после \x01\x14 или последние кириллические куски
    parts = re.split(r"[\x01\x14\x13\x15]+", line)
    clean = []
    for p in parts:
        p = p.strip()
        if not p or p.startswith("PAGEREF") or p.startswith("HYPERLINK") or p.startswith("TOC"):
            continue
        if re.search(r"[А-Яа-яЁё]", p):
            clean.append(p)
    if clean:
        return " ".join(clean)
    return line


def extract_up02_plain() -> str:
    with zipfile.ZipFile(SRC_UP, "r") as z:
        xml = z.read("word/document.xml")
    root = ET.fromstring(xml)
    ns = {"w": "http://schemas.openxmlformats.org/wordprocessingml/2006/main"}
    chunks: list[str] = []
    for t in root.findall(".//w:t", ns):
        if t.text:
            chunks.append(t.text)
        if t.tail:
            chunks.append(t.tail)
    return "\n".join(chunks)


def split_up02_sections(text: str) -> dict[str, str]:
    """Разбиение отчёта «уп 02» по уникальным якорям тела документа (не оглавление)."""
