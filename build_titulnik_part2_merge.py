def clean_text_for_word(s: str) -> str:
    """Удаляет управляющие символы и мусор из извлечённых строк .doc."""
    s = re.sub(r"[\x00-\x08\x0b\x0c\x0e-\x1f\uFFFD]", "", s)
    return s.strip()


def paragraph_xml(text: str, bold: bool = False, heading: bool = False) -> str:
    text = clean_text_for_word(text)
    if not text:
        return ""
    esc = html.escape(text)
    # OOXML: разбить по символу мягкого переноса не нужно
    r_pr = ""
    if bold or heading:
        r_pr = (
            "<w:rPr>"
            "<w:b/><w:bCs/>"
            "<w:rFonts w:ascii='Times New Roman' w:hAnsi='Times New Roman' w:cs='Times New Roman'/>"
            "<w:sz w:val='28'/><w:szCs w:val='28'/>"
            "</w:rPr>"
        )
    else:
        r_pr = (
            "<w:rPr>"
            "<w:rFonts w:ascii='Times New Roman' w:hAnsi='Times New Roman' w:cs='Times New Roman'/>"
            "<w:sz w:val='28'/><w:szCs w:val='28'/>"
            "</w:rPr>"
        )

    p_pr = (
        "<w:pPr>"
        "<w:spacing w:line='360' w:lineRule='auto'/>"
        "<w:ind w:firstLine='708'/>"
        "<w:jc w:val='both'/>"
        "</w:pPr>"
    )
    if heading:
        p_pr = (
            "<w:pPr>"
            "<w:spacing w:before='240' w:after='120' w:line='360' w:lineRule='auto'/>"
            "<w:ind w:firstLine='708'/>"
            "<w:jc w:val='both'/>"
            "</w:pPr>"
        )

    if not esc:
        return ""
    return f"<w:p>{p_pr}<w:r>{r_pr}<w:t xml:space='preserve'>{esc}</w:t></w:r></w:p>"


def page_break_xml() -> str:
    return (
        "<w:p><w:r><w:br w:type='page'/></w:r></w:p>"
    )


def build_document_xml(part1_lines: list[str], part2_lines: list[str], sect_pr_tail: str) -> bytes:
    parts: list[str] = []
    parts.append(
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<w:document xmlns:wpc="http://schemas.microsoft.com/office/word/2010/wordprocessingCanvas" '
        'xmlns:mc="http://schemas.openxmlformats.org/markup-compatibility/2006" '
        'xmlns:o="urn:schemas-microsoft-com:office:office" '
        'xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships" '
        'xmlns:m="http://schemas.openxmlformats.org/officeDocument/2006/math" '
        'xmlns:v="urn:schemas-microsoft-com:vml" '
        'xmlns:w10="urn:schemas-microsoft-com:office:word" '
        'xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main" '
        'xmlns:w14="http://schemas.microsoft.com/office/word/2010/wordml" '
        'xmlns:w15="http://schemas.microsoft.com/office/word/2012/wordml" '
        'xmlns:wne="http://schemas.microsoft.com/office/word/2006/wordml" '
        'xmlns:wp="http://schemas.openxmlformats.org/drawingml/2006/wordprocessingDrawing" '
        'xmlns:wps="http://schemas.microsoft.com/office/word/2010/wordprocessingShape" '
        'xmlns:wpg="http://schemas.microsoft.com/office/word/2010/wordprocessingGroup" '
        'mc:Ignorable="w14 w15 wp14">'
        "<w:body>"
    )

    started = False
    for line in part1_lines:
        cleaned = strip_toc_field_noise(line).strip()
        cleaned = clean_text_for_word(cleaned)
        if not cleaned:
            continue
        if not started:
            if "КГБПОУ" in cleaned or cleaned.startswith("ОТЧЕТ"):
                started = True
            else:
                continue
        # заголовки разделов верхнего уровня без абзацного отступа — упростим: всё с отступом по методичке
        is_bold_heading = cleaned in (
            "Содержание",
            "Введение",
            "Сведения о практике",
            "1 Основная часть",
        ) or re.match(r"^1\.\d", cleaned) or cleaned.startswith("1.6.") or cleaned.startswith("Рисунок")
        parts.append(paragraph_xml(cleaned, bold=is_bold_heading, heading=False))

    parts.append(page_break_xml())

    for i, line in enumerate(part2_lines):
        if not line.strip():
            continue
        if line.strip() in ("2. Индивидуальное задание", "ЗАКЛЮЧЕНИЕ", "СПИСОК ИСПОЛЬЗОВАННЫХ ИСТОЧНИКОВ"):
            parts.append(paragraph_xml(line.strip(), bold=True, heading=True))
        elif re.match(r"^2\.\d{1,2}\s", line.strip()):
            parts.append(paragraph_xml(line.strip(), bold=True, heading=True))
        else:
            parts.append(paragraph_xml(line.strip(), bold=False))

    parts.append(sect_pr_tail)
    parts.append("</w:body></w:document>")
    return "".join(parts).encode("utf-8")


