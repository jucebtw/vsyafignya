def get_sect_pr_from_template() -> str:
    with zipfile.ZipFile(TEMPLATE, "r") as z:
        xml = z.read("word/document.xml").decode("utf-8")
    i = xml.rfind("<w:sectPr")
    if i == -1:
        return "<w:sectPr><w:pgSz w:w='11906' w:h='16838'/><w:pgMar w:top='1134' w:right='851' w:bottom='1134' w:left='1701' w:header='708' w:footer='708' w:gutter='0'/></w:sectPr>"
    return xml[i : xml.find("</w:sectPr>", i) + len("</w:sectPr>")]


def main() -> None:
    full = decode_doc_utf16(SRC_DOC)
    idx = full.find(SPLIT_MARKER_BODY)
    if idx == -1:
        raise SystemExit("Маркер раздела 2 не найден")
    raw_part1 = full[:idx].replace("\x0c", "\n")
    part1_lines = [ln for ln in re.split(r"[\r\n]+", raw_part1) if ln.strip()]

    up_text = extract_up02_plain()
    up_sections = split_up02_sections(up_text)
    part2_lines = build_part2_content(up_sections)

    # статистика объёма
    p2_chars = sum(len(x) for x in part2_lines)
    print(f"Символов во 2-й части + заключение + список: {p2_chars}")

    sect_pr = get_sect_pr_from_template()
    doc_xml = build_document_xml(part1_lines, part2_lines, sect_pr)

    shutil.copyfile(TEMPLATE, OUT_DOCX)
    with zipfile.ZipFile(OUT_DOCX, "r") as zin:
        with zipfile.ZipFile(str(OUT_DOCX) + ".tmp", "w", zipfile.ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                if item.filename == "word/document.xml":
                    zout.writestr(item, doc_xml)
                else:
                    zout.writestr(item, zin.read(item.filename))
    Path(str(OUT_DOCX) + ".tmp").replace(OUT_DOCX)
    print(f"Готово: {OUT_DOCX}")


if __name__ == "__main__":
    main()
