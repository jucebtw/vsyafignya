def extract_between(s: str, start: str, end: str) -> str:
    if not s:
        return ""
    i = s.find(start)
    if i == -1:
        return ""
    i += len(start)
    if end:
        j = s.find(end, i)
        if j == -1:
            return s[i:].strip()
        return s[i:j].strip()
    return s[i:].strip()


